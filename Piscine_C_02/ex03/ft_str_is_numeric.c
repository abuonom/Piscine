int	ft_str_is_numeric(char *str)
{
	int	flag;
	int	i;

	i = 0;
	flag = 0;
	if (str[0] == '\0' || str[0] == ' ')
	{
		return (1);
	}
	else
	{
		while (str[i] != '\0')
		{
			if (str[i] >= '0' && str[i] <= '9')
				flag = 1;
			else
				return (0);
			i++;
		}
	}
	return (flag);
}
