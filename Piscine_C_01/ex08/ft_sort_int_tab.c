void	ft_sort_int_tab(int *tab, int size)
{
	int	c;
	int	s;

	c = 0;
	while (c <= size - 1)
	{
		if (tab[c] > tab[c + 1])
		{
			s = tab[c];
			tab[c] = tab[c + 1];
			tab[c + 1] = s;
			c = 0;
		}
		else
			c++;
	}
}
